SELECT
    ORD.PRODUCT_ID,
    PRO.PRODUCT_NAME,
    SUM(PRO.PRICE*ORD.AMOUNT) AS TOTAL_SALES
FROM FOOD_ORDER ORD 
JOIN FOOD_PRODUCT PRO
    ON ORD.PRODUCT_ID=PRO.PRODUCT_ID
WHERE 
    ORD.PRODUCE_DATE LIKE '2022-05%'
GROUP BY ORD.PRODUCT_ID
ORDER BY TOTAL_SALES DESC,
    PRO.PRODUCT_ID ASC

