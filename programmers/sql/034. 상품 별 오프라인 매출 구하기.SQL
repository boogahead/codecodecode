SELECT
    PRO.PRODUCT_CODE,
    SUM(SALES_AMOUNT*PRICE) AS SALES
FROM
    PRODUCT PRO
JOIN 
    OFFLINE_SALE OFF
    ON PRO.PRODUCT_ID=OFF.PRODUCT_ID
GROUP BY
    PRO.PRODUCT_ID
ORDER BY
    SALES DESC,
    PRO.PRODUCT_CODE ASC
